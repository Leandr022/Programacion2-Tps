package trabajopractico5.programacion2.Ejercicio13;

public class Alumno {
    private String nombre;
    private int legajo;

    public Alumno(String nombre, int legajo) {
        this.nombre = nombre;
        this.legajo = legajo;
    }

    public String getNombre() { return nombre; }
    public int getLegajo() { return legajo; }
}
